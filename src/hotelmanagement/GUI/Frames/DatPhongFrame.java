    private void sdtInputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sdtInputKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') ||
           (c == KeyEvent.VK_DELETE))) {
                getToolkit().beep();
                evt.consume();
        }
    }//GEN-LAST:event_sdtInputKeyTyped

