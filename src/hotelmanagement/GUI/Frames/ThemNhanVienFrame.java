    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        this.maleInput.setActionCommand("1");
        this.femaleInput.setActionCommand("0");

        String hoten = this.tenInput.getText();
        int gioiTinh = Integer.parseInt(this.gioiTinhInput.getSelection().getActionCommand());
        String sdt = this.sdtInput.getText();
        String diaChi = this.diaChiInput.getText();
        
        NhanVienDTO nv = new NhanVienDTO();
        nv.setTenNhanVien(hoten);
        nv.setSoDienThoai(sdt);
        nv.setDiaChi(diaChi);
        nv.setGioiTinh(gioiTinh);
        nv.setNgaySinh(this.ngaySinhInput.getDate());
        
        JFrame frame = new JFrame();
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        if (NhanVienBLL.getInstance().addNhanVien(nv) == 1){
            JOptionPane.showMessageDialog(frame,
                    "Thêm nhân viên thành công!",
                    "Thành công",
                    JOptionPane.INFORMATION_MESSAGE);
            NhanVienGUI.getInstance().loadData();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(frame,
                    "Thêm nhân viên thất bại!",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1MouseClicked

