    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String cccd = this.cccdInput.getText();
        String gioiTinh = this.gioiTinhInput.getSelection().getActionCommand();
        String hoten = this.hotenInput.getText();
        String sdt = this.sdtInput.getText();
        String soLuong = this.slPhongInput.getValue().toString();
        ArrayList<Integer> loaiPhong = new ArrayList<Integer>();
        if (this.phongDonCheckBox.isSelected()) loaiPhong.add(1);
        if (this.phongDoiCheckBox.isSelected()) loaiPhong.add(2);
        if (this.VIPCheckBox.isSelected()) loaiPhong.add(3);
        
        
        Date beginDate = this.ngayBatDauInput.getDate();
        Date endDate = this.ngayKetThucInput.getDate();
        
        String note = this.ghiChuInput.getText();
        
        JFrame frame = new JFrame();
        frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        if (utils.compareDates(beginDate, endDate) != -1) { 
            JOptionPane.showMessageDialog(frame,
                    "Ngày đặt phòng phải trước ngày dự kiến kết thúc!",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            DanhSachPhongGUI.getInstance().loadData();
            return;
        } 

        if (utils.compareDates(beginDate, new Date()) == -1 || utils.compareDates(endDate, new Date()) != 1) {
            JOptionPane.showMessageDialog(frame,
                    "Ngày đặt phòng và ngày kết thúc phải lớn hơn hoặc bằng ngày hiện tại.",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            DanhSachPhongGUI.getInstance().loadData();
            return;
        }

        if (cccdInput.getText().length() != 9 && cccdInput.getText().length() != 12) {
            JOptionPane.showMessageDialog(frame,
                    "Số CCCD Không Hợp Lệ",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            DanhSachPhongGUI.getInstance().loadData();
            return;
        }

        if (sdtInput.getText().length() != 10) {
            JOptionPane.showMessageDialog(frame,
                    "Số điện thoại Không Hợp Lệ",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            DanhSachPhongGUI.getInstance().loadData();
            return;
        }
        
        
        
        
    }//GEN-LAST:event_jButton1MouseClicked

    private void cccdInputKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cccdInputKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') ||
           (c == KeyEvent.VK_BACK_SPACE) ||
           (c == KeyEvent.VK_DELETE))) {
                getToolkit().beep();
                evt.consume();
        }
    }//GEN-LAST:event_cccdInputKeyTyped

